function drawMap() {
    map = showMap1()
    // DIBUJAR MAPA.
    for (let fila = 0; fila < map.length; fila++) {
        for (let columna = 0; columna < map[fila].length; columna++) {

            if (map[fila][columna] === 1) {
                // Esto hace practicamente lo mismo que en el ejmplo de si es === 1, pero cambiando la imagen.
                const groundBlock = document.createElement('div')

                document.querySelector('.ground').appendChild(groundBlock)
                groundBlock.classList.add('suelo')
                groundBlock.style.backgroundColor = '#E2A23E'
                // groundBlock.style.backgroundSize = 'cover'
                groundBlock.style.top = fila * BLOCK_SIZE + 'px'
                groundBlock.style.left = (columna * BLOCK_SIZE) + 'px'
                groundBlock.style.width =  100 + '%'
                groundBlock.style.height = BLOCK_HEIGHT + 'px'
                groundBlock.style.position = 'absolute'
                // COLLIDABLE.push(groundBlock)
            
            } else if (map[fila][columna] === 2) {
                const trees = document.createElement('div');

                // if (level_1_finished) {
                //     document.querySelector('.filasNivel2').appendChild(mapBlock2);
                //     document.querySelector('.fila1').style.display = 'none';
                // } else {
                document.querySelector('.trees').appendChild(trees);
                // }
                trees.style.zIndex = 100
                trees.style.backgroundImage = 'url(../img/tree.png)';
                trees.style.backgroundSize = 'cover';
                trees.style.top = 500 + 'px';
                trees.style.left = (columna * BLOCK_SIZE) + 'px';
                trees.style.width = TREE_WIDTH + 'px';
                trees.style.height = TREE_HEIGHT + 'px';
                trees.style.position = 'absolute';
                // COLLIDABLE.push(mapBlock2);
                } else if (map[fila][columna] === 3) {
                const clouds = document.createElement('div')

                document.querySelector('.clouds').appendChild(clouds)
                clouds.style.backgroundImage = 'url(../img/cloud.png)'
                clouds.style.backgroundSize = 'cover'
                clouds.style.top = fila * BLOCK_SIZE + 'px'
                clouds.style.left = (columna * BLOCK_SIZE) + 'px'
                clouds.style.width =  CLOUD_WIDTH + 'px'
                clouds.style.height = CLOUD_HEIGHT + 'px'
                clouds.style.position = 'absolute'
                // COLLIDABLE_FINAL_LEVEL1.push(mapBlock3)
                // COLLIDABLE.push(mapBlock3)

            } 
            else if (map[fila][columna] === 4) {
                const herb = document.createElement('div')

                document.querySelector('.herb').appendChild(herb)
                herb.style.zIndex = 1
                herb.style.backgroundColor = '#3DD178'
                // herb.style.backgroundSize = 'cover'
                herb.style.top = 888 + 'px'
                herb.style.left = (columna * BLOCK_SIZE) + 'px'
                herb.style.width =  HERB_WIDTH + '%'
                herb.style.height = HERB_HEIGHT + 'px'
                herb.style.position = 'absolute'
                // COLLIDABLE_FINAL_LEVEL1.push(mapBlock3)
                // COLLIDABLE.push(mapBlock3)

            } 
            

        }
    }

}

