const BLOCK_SIZE = 37
const BLOCK_WIDTH_LAST_ONE = 16
const BLOCK_HEIGHT = 81
const CLOUD_HEIGHT = 134
const CLOUD_WIDTH = 238
const GRASS_HEIGHT = 34
const GRASS_WIDTH = 100
const TREE_HEIGHT = 413
const TREE_WIDTH = 310
const MOUNTAIN_HEIGHT = 293

let cloudsArray = []
let cloudsPositionX = []
let cloudsPositionY = []

drawMap()
function draw() {
    for (let i = 0; i < cloudsArray.length; i++) {
        cloudsArray[i].style.left = cloudsPositionX[i] + 'px'
        cloudsArray[i].style.top = cloudsPositionY[i] + 'px'
    }
}
function update() {
    draw()

}

function animate() {
    requestAnimationFrame(animate)
    update()
}
animate()
function drawMap() {
    map = showMap1()
    // DIBUJAR MAPA.
    for (let fila = 0; fila < map.length; fila++) {
        for (let columna = 0; columna < map[fila].length; columna++) {

            if (map[fila][columna] === 1) {
                const groundBlock = document.createElement('div')

                document.querySelector('.ground').appendChild(groundBlock)
                groundBlock.classList.add('suelo')
                groundBlock.style.backgroundColor = '#E2A23E'
                groundBlock.style.top = fila * BLOCK_SIZE + 'px'
                groundBlock.style.left = (columna * BLOCK_SIZE) + 'px'
                groundBlock.style.width =  100 + '%'
                groundBlock.style.height = BLOCK_HEIGHT + 'px'
                groundBlock.style.position = 'absolute'
            
            } else if (map[fila][columna] === 2) {
                const trees = document.createElement('div');

                document.querySelector('.trees').appendChild(trees);
                trees.style.zIndex = 100
                trees.style.backgroundImage = 'url(../img/tree.png)';
                trees.style.backgroundSize = 'cover';
                trees.style.top = 500 + 'px';
                trees.style.left = (columna * BLOCK_SIZE) + 'px';
                trees.style.width = TREE_WIDTH + 'px';
                trees.style.height = TREE_HEIGHT + 'px';
                trees.style.position = 'absolute';
                } else if (map[fila][columna] === 3) {
                const clouds = document.createElement('div')

                document.querySelector('.clouds').appendChild(clouds)
                clouds.style.backgroundImage = 'url(../img/cloud.png)'
                clouds.style.backgroundSize = 'cover'
                clouds.style.top = fila * BLOCK_SIZE + 'px'
                clouds.style.left = (columna * BLOCK_SIZE) + 'px'
                clouds.style.width =  CLOUD_WIDTH + 'px'
                clouds.style.height = CLOUD_HEIGHT + 'px'
                clouds.style.position = 'absolute'
                cloudsArray.push(clouds)
                cloudsPositionX.push(parseInt(clouds.style.left.replace('px', '')))
                cloudsPositionY.push(clouds.style.top.replace('px', ''))
            } 
            else if (map[fila][columna] === 4) {
                const grass = document.createElement('div')

                document.querySelector('.grass').appendChild(grass)
                grass.style.zIndex = 1
                // grass.style.backgroundColor = '#3DD178'
                grass.style.backgroundImage = 'url(../img/grass.png)'
                // grass.style.backgroundSize = 'cover'
                grass.style.top = 888 + 'px'
                grass.style.left = (columna * BLOCK_SIZE) + 'px'
                grass.style.width =  GRASS_WIDTH + '%'
                grass.style.height = GRASS_HEIGHT + 'px'
                grass.style.position = 'absolute'

            } 
            else if (map[fila][columna] === 5) {
                const mountain = document.createElement('div')

                document.querySelector('.mountains').appendChild(mountain)
                mountain.style.backgroundImage = 'url(../img/mountain.png)'
                mountain.style.backgroundSize = 'cover'
                mountain.style.top = fila * BLOCK_SIZE + 'px'
                mountain.style.left = (columna * BLOCK_SIZE) + 'px'
                mountain.style.width =  TREE_WIDTH + 'px'
                mountain.style.height = MOUNTAIN_HEIGHT + 'px'
                mountain.style.position = 'absolute'

            } 

        }
    }

}

